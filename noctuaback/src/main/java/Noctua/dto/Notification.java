package Noctua.dto;

public class Notification {
	
	private String title; //titulo da notificação
	
	private String body; //corpo da notificação

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getBody() {
		return body;
	}

	public void setBody(String body) {
		this.body = body;
	}
	
	
}
